    private void addLabel(Penginapan penginapan) {
        JPanel item = new JPanel();
        Date date1 = (Date) Tanggalcheckin.getValue();
        Date date2 = (Date) TanggalCehckout.getValue();
        LocalDate localDate1 = date1.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
        LocalDate localDate2 = date2.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
        DateTimeFormatter simple = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String checkin = localDate1.format(simple);
        String checkout = localDate2.format(simple);
        int days = (int) ChronoUnit.DAYS.between(localDate1, localDate2);
        int capacity = penginapan.getOrderedAt(checkin);
        String dateTemp = "";
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate date = LocalDate.parse(checkin, formatter);
        for (int i = 1; i <= days; i++) {
            LocalDate nextDate = date.plusDays(i);
            dateTemp = formatter.format(nextDate);
            int tempTanggal = penginapan.getOrderedAt(dateTemp);
            if (capacity < tempTanggal) {
                capacity = tempTanggal;
            }
        }
        item.setLayout(new BoxLayout(item, BoxLayout.X_AXIS));
        item.setBackground(new java.awt.Color(70, 73, 75));
        item.setForeground(new java.awt.Color(255, 255, 255));
        item.setMaximumSize(new java.awt.Dimension(615, 50));
        item.setMinimumSize(new java.awt.Dimension(615, 50));
        item.setPreferredSize(new java.awt.Dimension(615, 50));
        item.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        JLabel transportLabel = new JLabel("  " + penginapan.nama);
        transportLabel.setForeground(new java.awt.Color(225, 225, 225));
        transportLabel.setMaximumSize(new java.awt.Dimension(200, 20));
        transportLabel.setMinimumSize(new java.awt.Dimension(200, 20));
        transportLabel.setFont(new java.awt.Font("Montserrat Medium", 0, 12));
        JLabel Destination = new JLabel(penginapan.jenis);
        Destination.setForeground(new java.awt.Color(225, 225, 225));
        Destination.setMaximumSize(new java.awt.Dimension(100, 20));
        Destination.setFont(new java.awt.Font("Montserrat Medium", 0, 12));
        JLabel Harga = new JLabel("Rp" + penginapan.harga);
        Harga.setForeground(new java.awt.Color(225, 225, 225));
        Harga.setMaximumSize(new java.awt.Dimension(120, 20));
        Harga.setFont(new java.awt.Font("Montserrat Medium", 0, 12));
        JLabel kursi = new JLabel(String.valueOf(penginapan.kamar - capacity));
        kursi.setForeground(new java.awt.Color(225, 225, 225));
        kursi.setMaximumSize(new java.awt.Dimension(50, 20));
        kursi.setFont(new java.awt.Font("Montserrat Medium", 0, 12));
        JButton pesan = new JButton();
        pesan.setText("Pesan");
        pesan.setBackground(new java.awt.Color(70, 73, 75));
        pesan.setForeground(new java.awt.Color(255, 255, 255));

        item.add(transportLabel);
        item.add(Destination);
        item.add(Harga);
        item.add(kursi);
        jPanel4.add(item);
        if (penginapan.kamar - capacity > 0) {
            pesan.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    pesanbuttonAcvtion(evt, penginapan);
                }
            });
            item.add(pesan);
        }
    }
    

    private void pesanbuttonAcvtion(java.awt.event.ActionEvent evt, Penginapan penginapan) {
        Date date1 = (Date) Tanggalcheckin.getValue();
        Date date2 = (Date) TanggalCehckout.getValue();
        LocalDate localDate1 = date1.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
        LocalDate localDate2 = date2.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
        DateTimeFormatter simple = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String checkin = localDate1.format(simple);
        String checkout = localDate2.format(simple);
        int days = (int) ChronoUnit.DAYS.between(localDate1, localDate2);
        int kamar = (int) Kamarspin.getValue();
        if (days <= 0) {
            JOptionPane.showMessageDialog(null,
                    "Atur Tanggal Check-out!");
        } else {
            if (kamar > penginapan.kamar || penginapan.kamar <= 0) {
                JOptionPane.showMessageDialog(null,
                        "Kapasitas Penuh!");
            } else {
                new DataDiriTamuGui(this, home, akun, checkin, checkout, kamar, penginapan, days);
                this.setVisible(false);
            }
        }

    }

