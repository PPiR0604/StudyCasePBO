    private void searchAllData() {
        jPanel4.removeAll(); // Clear previous results
        for (Penginapan temp : data) {
            if (temp.jenis.equals(jenisComboBox.getSelectedItem().toString())) {
                addLabel(temp);
            }
        }
        jPanel4.revalidate(); // Refresh the panel
        jPanel4.repaint(); // Repaint to update the
    }

    private void sortSpinActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_sortSpinActionPerformed
        if (sortSpin.getSelectedItem().equals("Discending by Price")) {
            Collections.sort(data, new PDiscendingPrice());
            searchAllData();
        } else if (sortSpin.getSelectedItem().equals("Ascending by Price")) {
            Collections.sort(data, new PAscendingPrice());
            searchAllData();
        } else if (sortSpin.getSelectedItem().equals("Discending by Capacity")) {
            Collections.sort(data, new PDiscendingCapacity());
            searchAllData();
        } else {
            Collections.sort(data, new PAscendingCapacity());
            searchAllData();
        }
    }// GEN-LAST:event_sortSpinActionPerformed

